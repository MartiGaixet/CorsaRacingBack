        [HttpPost]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Create([Bind("Id,Name,email,Country,Rating")] User user)
        {
            if (ModelState.IsValid)
            {
                _userService.AddUser(user);

                return RedirectToAction(nameof(Index));
            }
            return View(user);
        }

